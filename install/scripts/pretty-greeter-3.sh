#!/bin/sh

echo -e "\033[1;36m"
cat << 'EOF'
           _____   ______         _                       
     /\   |_   _| |  ____|       | |                      
    /  \    | |   | |__ ___  __ _| |_ _   _ _ __ ___  ___ 
   / /\ \   | |   |  __/ _ \/ _` | __| | | | '__/ _ \/ __|
  / ____ \ _| |_  | | |  __/ (_| | |_| |_| | | |  __/\__ \
 /_/    \_\_____| |_|  \___|\__,_|\__|\__,_|_|  \___||___/                                                    
EOF
echo -e "\033[0m"

chars="////////////////////////////////////////////////////////////////////////////////"
colors=(31 33 32 36 34 35) # red, yellow, green, cyan, blue, magenta

for ((i=0; i<${#chars}; i++)); do
    color=${colors[i % ${#colors[@]}]}
    printf "\033[1;%sm%s" "$color" "${chars:$i:1}"
done

echo -e "\033[0m"  # Reset color at the end