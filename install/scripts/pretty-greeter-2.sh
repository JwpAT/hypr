#!/bin/sh

echo -e "\033[1;36m"
cat << 'EOF'
  _____           _        _ _   _____           _                         
 |_   _|         | |      | | | |  __ \         | |                        
   | |  _ __  ___| |_ __ _| | | | |__) |_ _  ___| | ____ _  __ _  ___  ___ 
   | | | '_ \/ __| __/ _` | | | |  ___/ _` |/ __| |/ / _` |/ _` |/ _ \/ __|
  _| |_| | | \__ \ || (_| | | | | |  | (_| | (__|   < (_| | (_| |  __/\__ \
 |_____|_| |_|___/\__\__,_|_|_| |_|   \__,_|\___|_|\_\__,_|\__, |\___||___/
                                                            __/ |          
                                                           |___/           
EOF
echo -e "\033[0m"

chars="////////////////////////////////////////////////////////////////////////////////"
colors=(31 33 32 36 34 35) # red, yellow, green, cyan, blue, magenta

for ((i=0; i<${#chars}; i++)); do
    color=${colors[i % ${#colors[@]}]}
    printf "\033[1;%sm%s" "$color" "${chars:$i:1}"
done

echo -e "\033[0m"  # Reset color at the end
