#!/bin/sh

echo -e "\033[1;36m"
cat << 'EOF'
  ______             _     _         _____                 _               
 |  ____|           | |   | |       / ____|               (_)              
 | |__   _ __   __ _| |__ | | ___  | (___   ___ _ ____   ___  ___ ___  ___ 
 |  __| | '_ \ / _` | '_ \| |/ _ \  \___ \ / _ \ '__\ \ / / |/ __/ _ \/ __|
 | |____| | | | (_| | |_) | |  __/  ____) |  __/ |   \ V /| | (_|  __/\__ \
 |______|_| |_|\__,_|_.__/|_|\___| |_____/ \___|_|    \_/ |_|\___\___||___/
EOF
echo -e "\033[0m"

chars="////////////////////////////////////////////////////////////////////////////////"
colors=(31 33 32 36 34 35) # red, yellow, green, cyan, blue, magenta

for ((i=0; i<${#chars}; i++)); do
    color=${colors[i % ${#colors[@]}]}
    printf "\033[1;%sm%s" "$color" "${chars:$i:1}"
done

echo -e "\033[0m"  # Reset color at the end