#!/bin/sh

echo -e "\033[1;36m"
cat << 'EOF'
  _____           _        _ _   _______                
 |_   _|         | |      | | | |__   __|               
   | |  _ __  ___| |_ __ _| | |    | |_   _ _ __   ___  
   | | | '_ \/ __| __/ _` | | |    | | | | | '_ \ / _ \ 
  _| |_| | | \__ \ || (_| | | |    | | |_| | |_) |  __/ 
 |_____|_| |_|___/\__\__,_|_|_|    |_|\__, | .__/ \___| 
                                       __/ | |          
                                      |___/|_|
EOF
echo -e "\033[0m"

chars="////////////////////////////////////////////////////////////////////////////////"
colors=(31 33 32 36 34 35) # red, yellow, green, cyan, blue, magenta

for ((i=0; i<${#chars}; i++)); do
    color=${colors[i % ${#colors[@]}]}
    printf "\033[1;%sm%s" "$color" "${chars:$i:1}"
done

echo -e "\033[0m"  # Reset color at the end